package TNOOP;

import java.util.Scanner;

public class Sum {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        double s1=sc.nextByte(),s2=sc.nextByte();
        System.out.println(s1+s2);
        System.out.println(s1-s2);
        System.out.println(s1*s2);
        System.out.println(s1/s2);
    }
}
